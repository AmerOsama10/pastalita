    public void show_cash()
    {
        ArrayList<Cash> list=getCashList();
        DefaultTableModel model=(DefaultTableModel)cashierstable.getModel();
        Object[] row=new Object[6];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getid();
            row[1]=list.get(i).getname();
            row[2]=list.get(i).getphone();
            row[3]=list.get(i).getaddress();
            row[4]=list.get(i).getemail();
            row[5]=list.get(i).getpassword();
            
            model.addRow(row);
        }  
    }
    
    public void executeSQlQuery(String query, String message)
   {
       Connection con = getConnection();
       Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)cashierstable.getModel();
               model.setRowCount(0);
               show_cash();
               txtid.setText("");
               txtname.setText("");
               txtphone.setText("");
               txtaddress.setText("");
               txtemail.setText("");
               txtpassword.setText("");
               
               JOptionPane.showMessageDialog(null, "تم "+message+"الكاشير بنجاح  "+"  ");
           }else{
               JOptionPane.showMessageDialog(null, "لم يتم "+message+"الكاشير ");
           }
       }
       catch(SQLIntegrityConstraintViolationException  ex){
           if(message.equals("حذف  "))
               JOptionPane.showMessageDialog(null, "لم يتم الحذف قد تم استخدام الكاشير لعمل اوردرات بالفعل");
           else
               JOptionPane.showMessageDialog(null, "اسم المستخدم موجود بالفعل \n يرجي ادخال اسم مستخدم مختلف");
          
       }
       catch(Exception ex)
       {
          
             System.out.print(ex.toString());
               JOptionPane.showMessageDialog(null, "حدد ماذا تريد");
       }
   }
    
