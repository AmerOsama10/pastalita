    public ArrayList<Cash>getCashList()
    {
        ArrayList<Cash>cashList=new ArrayList<Cash>();
        Connection connection= getConnection();
        String query="SELECT * FROM `cashiers`;";
        Statement st;
        ResultSet rs;
        try{
            st=connection.createStatement();
            rs=st.executeQuery(query);
            Cash cash;
            while(rs.next())
            {
                cash = new Cash(rs.getInt("cashier_id"),rs.getString("cashier_name"),rs.getString("cashier_phone"),rs.getString("cashier_address"),rs.getString("cashier_email"),rs.getString("cashier_password"));
               cashList.add(cash);
            }
            
        }
        catch(Exception e)
        {
         e.printStackTrace();
        }
        return cashList;
    }
